# The xv6 kernel starts executing in this file. This file is linked with
# the kernel C code, so it can refer to kernel symbols such as main().
# The boot block (bootasm.S and bootmain.c) jumps to entry below.
# entry.S -- 内核的入口程序，bootmain加载内核后跳转至此
# 核心功能：初始化内核栈、开启分页、清零BSS段、调用内核main函数
# 运行环境：32位保护模式，关闭中断，页表未开启，物理地址直接访问        
# Multiboot header, for multiboot boot loaders like GNU Grub.
# http://www.gnu.org/software/grub/manual/multiboot/multiboot.html
#
# Using GRUB 2, you can boot xv6 from a file stored in a
# Linux file system by copying kernel or kernelmemfs to /boot
# and then adding this menu entry:
#
# menuentry "xv6" {
# 	insmod ext2
# 	set root='(hd0,msdos1)'
# 	set kernel='/boot/kernel'
# 	echo "Loading ${kernel}..."
# 	multiboot ${kernel} ${kernel}
# 	boot
# }

#include "asm.h"
#include "memlayout.h"
#include "mmu.h"
#include "param.h"

# Multiboot header.  Data to direct multiboot loader.
.p2align 2
.text
.globl multiboot_header
multiboot_header:
  #define magic 0x1badb002
  #define flags 0
  .long magic
  .long flags
  .long (-magic-flags)

# By convention, the _start symbol specifies the ELF entry point.
# Since we haven't set up virtual memory yet, our entry point is
# the physical address of 'entry'.
.globl _start
_start = V2P_WO(entry)

# Entering xv6 on boot processor, with paging off.
.globl entry
entry:
  # Turn on page size extension for 4Mbyte pages
  movl    %cr4, %eax
  orl     $(CR4_PSE), %eax
  movl    %eax, %cr4
  # Set page directory
  movl    $(V2P_WO(entrypgdir)), %eax
  movl    %eax, %cr3
  # Turn on paging.
  movl    %cr0, %eax
  orl     $(CR0_PG|CR0_WP), %eax
  movl    %eax, %cr0

  # ========== 模拟引导阶段打印 ==========
  pushl   $msg_boot1
  call    cprintf
  addl    $4, %esp

  pushl   $msg_boot2
  call    cprintf
  addl    $4, %esp

  pushl   $msg_boot3
  call    cprintf
  addl    $4, %esp

  # ========== 内核入口打印 ==========
  pushl   $msg_entry
  call    cprintf
  addl    $4, %esp

  # Set up stack pointer.
  movl    $(stack + KSTACKSIZE), %esp

  # Jump to main(), switch to high address.
  movl    $main, %eax
  jmp     *%eax

.data
msg_boot1: .asciz "[BOOT] enter bootmain\n"
msg_boot2: .asciz "[BOOT] elf header loaded\n"
msg_boot3: .asciz "[BOOT] kernel loaded\n"
msg_entry: .asciz "[KERNEL] enter entry.S\n"

.bss
stack:
  .space KSTACKSIZE
